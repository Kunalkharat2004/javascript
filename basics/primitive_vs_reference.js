// Primitive type
let num1=6;
let num2= num1;
console.log(num1);
console.log(num2);
num1++;
console.log("After increment of num1");
console.log(num1);
console.log(num2);
let s1="Kunal";
let s2=s1;
s1="Pranjal";
console.log(s1);
console.log(s2);

// Reference type
array1=["Mango","Apple","Strawberry","Pineapple"];
array2=array1;
console.log(array1);
console.log(array2);
array1.push("Orange");
console.log("After pushing 'Orange' to array1");
console.log(array1);
console.log(array2);