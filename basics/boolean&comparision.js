// Boolean
let num1=8;
let num2=7;
console.log(num1<=num2);

// == vs ===
num1="7";
console.log(num1==num2);   // == doesn't care about the datatype 
console.log(num1===num2);  // === Care about the datatype

// != vs !==
console.log(num1 != num2);  // != doesn't care about datatype
console.log(num1!==num2);  // !== Care about datatype